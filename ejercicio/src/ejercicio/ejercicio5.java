package ejercicio;

public class ejercicio5 {
    public static void main(String[] args) {
       
    	// Declaración e inicialización de la variable para el radio
        double radio = 7.5;  

        // Calcular el perímetro del círculo usando la fórmula: Perímetro = 2 * π * radio
        double perimetro = 2 * Math.PI * radio;

        // Mostrar el resultado en pantalla
        System.out.println("El perímetro del círculo con radio " + radio + " es: " + perimetro);
    }
}

